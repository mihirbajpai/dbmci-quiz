package com.example.dbmciquiz.view.theme

import androidx.compose.ui.unit.dp

/**
 * The app's spacing scale (4dp grid). Screens and components pad and gap with these tokens instead
 * of raw dp so spacing stays consistent. Genuinely one-off dimensions (a Lottie size, a fixed
 * control height) keep their literal value.
 */
object Spacing {
    val xSmall = 4.dp
    val small = 8.dp
    val medium = 16.dp
    val large = 24.dp
    val xLarge = 32.dp
}
